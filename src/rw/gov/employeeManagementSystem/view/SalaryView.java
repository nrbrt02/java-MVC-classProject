    private void updateSalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateSalaryActionPerformed
        if (allsalaries.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select row to update");
        } else if (JName.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Select Job");
        } else if (amount.getText().trim().isEmpty() || !amount.getText().matches("^[0-9]*$")) {
            JOptionPane.showMessageDialog(this, "Amount Can't be empty\nOnly digits ");
        } else if (bonus.getText().trim().isEmpty() || !amount.getText().matches("^[0-9]*$")) {
            JOptionPane.showMessageDialog(this, "Bonus Can't be empty \nOnly digits ");
        } else {
            int rowSelected = allsalaries.getSelectedRow();
            DefaultTableModel tbModel = (DefaultTableModel) allsalaries.getModel();

            int theId = Integer.parseInt(tbModel.getValueAt(rowSelected, 0).toString());
//            int theamount = Integer.parseInt(tbModel.getValueAt(rowSelected, 2).toString());
//            int theanual = theamount * 12;
//            int thebonus = Integer.parseInt(tbModel.getValueAt(rowSelected, 4).toString());
////            int thejobId = Integer.parseInt(tbModel.getValueAt(rowSelected, 1).toString());

            Salary theSalary = new Salary();
            SalaryDao theSalaryDao = new SalaryDao();

            theSalary.setJobName(JName.getSelectedItem().toString());
            theSalary.setAmount(Integer.parseInt(amount.getText()));
            theSalary.setAnual(Integer.parseInt(amount.getText()) * 12);
            theSalary.setBonus(Integer.parseInt(bonus.getText()));
            theSalary.setId(theId);

            Integer rowAffected = theSalaryDao.update(theSalary);
            if (rowAffected != null) {
                JOptionPane.showMessageDialog(this, "Salary Updated");
                JName.setSelectedIndex(0);
                amount.setText("");
                bonus.setText("");
                allSalaryRetrived();
            } else {
                JOptionPane.showMessageDialog(this, "salary not saved");
            }
        }

    }//GEN-LAST:event_updateSalaryActionPerformed
    private void allSalaryRetrived() {
        SalaryDao saleryDao = new SalaryDao();

        ResultSet rs = saleryDao.allSalaries();
        DefaultTableModel tbModel = (DefaultTableModel) allsalaries.getModel();
        tbModel.setRowCount(0);

        try {
            while (rs.next()) {
                String id = rs.getString("id");
                String name = rs.getString("job_name");
                String amount = rs.getString("amount");
                String anual = rs.getString("anual");
                String bonus = rs.getString("bonus");
//                String total = Integer.parseInt(anual) + Integer.parseInt(bonus);

                String tData[] = {id, name, amount, anual, bonus};

                tbModel.addRow(tData);

            }

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    private void addSalary1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addSalary1ActionPerformed
        if (allsalaries.getSelectedRow() != -1) {
            JOptionPane.showMessageDialog(this, "Unselect Selected row to Insert");
        } else if (JName.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Select Job");
        } else if (amount.getText().trim().isEmpty() || !amount.getText().matches("^[0-9]*$")) {
            JOptionPane.showMessageDialog(this, "Amount Can't be empty\nOnly digits ");
        } else if (bonus.getText().trim().isEmpty() || !amount.getText().matches("^[0-9]*$")) {
            JOptionPane.showMessageDialog(this, "Bonus Can't be empty \nOnly digits ");
        } else {
            try {
                Salary newSalary = new Salary();

                int theamount = Integer.parseInt(amount.getText());
                int theanual = theamount * 12;
                int thebonus = Integer.parseInt(bonus.getText());

                newSalary.setJobName(JName.getSelectedItem().toString());
                newSalary.setAmount(theamount);
                newSalary.setAnual(theanual);
                newSalary.setBonus(thebonus);

                SalaryDao newSalaryDao = new SalaryDao();
                Integer rowAffected = newSalaryDao.register(newSalary);

                if (rowAffected != null) {
                    JOptionPane.showMessageDialog(this, "Salary Added");
                    JName.setSelectedIndex(0);
                    amount.setText("");
                    bonus.setText("");
                    allSalaryRetrived();
                } else {
                    JOptionPane.showMessageDialog(this, "salary not added");
                }
            } catch (Exception ex) {
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_addSalary1ActionPerformed



    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed

        if (allJobs.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select a Row to delete");
        } else {
            int choice = JOptionPane.showConfirmDialog(null, "Are U sure To delete", "confirm", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                int rowSelected = allJobs.getSelectedRow();
                DefaultTableModel tbModel = (DefaultTableModel) allJobs.getModel();
                int theId = Integer.parseInt(tbModel.getValueAt(rowSelected, 0).toString());

                JobDao theJobDao = new JobDao();
                Integer rowsAffected = theJobDao.delete(theId);

                if (rowsAffected != null) {
                    JOptionPane.showMessageDialog(this, "JOB Deleted");
                    jName.setText("");
                    description.setText("");
                    departmentName.setSelectedIndex(0);
                    allJobsRetrived();
                } else {
                    JOptionPane.showMessageDialog(this, "JOB NOT Deleted");
                }
            }
        }

    }//GEN-LAST:event_deleteBtnActionPerformed

    private void editBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editBtnActionPerformed
        if (allJobs.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select a Row to update");
        } else if (departmentName.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Select Department");
        } else if (jName.getText().trim().isEmpty() || jName.getText().length() < 4) {
            JOptionPane.showMessageDialog(this, "Name not Valid \n Make sure Not Empty\n Make sure length not less than 4 chars");
        } else if (description.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Description not Valid ");
        } else {

            int rowSelected = allJobs.getSelectedRow();
            DefaultTableModel tbModel = (DefaultTableModel) allJobs.getModel();
            int theId = Integer.parseInt(tbModel.getValueAt(rowSelected, 0).toString());
            Job theJob = new Job();

            theJob.setId(theId);
            theJob.setName(jName.getText());
            theJob.setDepartmentName(departmentName.getSelectedItem().toString());
            theJob.setDescription(description.getText());

            JobDao theJobDao = new JobDao();
            Integer rowsAffected = theJobDao.update(theJob);

            if (rowsAffected != null) {
                JOptionPane.showMessageDialog(this, "JOB UPDATED");
                jName.setText("");
                description.setText("");
                departmentName.setSelectedIndex(0);
                allJobsRetrived();
            } else {
                JOptionPane.showMessageDialog(this, "JOB NOT UPDATED");
            }
        }
    }//GEN-LAST:event_editBtnActionPerformed

    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        if (allJobs.getSelectedRow() != -1) {
            JOptionPane.showMessageDialog(this, "Unselect Selected row");
        } else if (departmentName.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this, "Select Department");
        } else if (jName.getText().trim().isEmpty() || jName.getText().length() < 4) {
            JOptionPane.showMessageDialog(this, "Name not Valid \n Make sure Not Empty\n Make sure length not less than 4 chars");
        } else if (description.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Description not Valid ");
        } else {
            Job job = new Job();

            job.setName(jName.getText());
            job.setDepartmentName(departmentName.getSelectedItem().toString());
            job.setDescription(description.getText());

            JobDao dptDao = new JobDao();
            Integer rowAffected = dptDao.register(job);

            if (rowAffected != null) {
                JOptionPane.showMessageDialog(this, "NEW JOB SAVED");
                jName.setText("");
                departmentName.setSelectedIndex(0);
                description.setText("");
                allJobsRetrived();
            } else {
                JOptionPane.showMessageDialog(this, "NEW JOB NOT SAVED");
            }
        }
    }//GEN-LAST:event_saveBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed

        JobDao jobDao = new JobDao();

        DefaultTableModel tbModel = (DefaultTableModel) allJobs.getModel();

        try {
            if (searchTxt.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Search Text can't be Empty");
                searchTxt.setText("");
            } else {
                ResultSet rs = jobDao.search(searchTxt.getText());
                tbModel.setRowCount(0);
                if (rs.isBeforeFirst()) {
                    while (rs.next()) {
                        String id = rs.getString("id");
                        String name = rs.getString("name");
                        String dname = rs.getString("department");
                        String description = rs.getString("description");

                        String tData[] = {id, name, dname, description};

                        tbModel.addRow(tData);

                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No Results");
                }

            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_searchBtnActionPerformed


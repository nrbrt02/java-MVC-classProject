    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        DepartmentDao dptDao = new DepartmentDao();
        DefaultTableModel tbModel = (DefaultTableModel) allDepartment.getModel();

        try {
            if (searchTxt.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Search Can't be Empty");
                searchTxt.setText("");
            } else {
                ResultSet rs = dptDao.searchDepartments(searchTxt.getText());
                tbModel.setRowCount(0);
                if (rs.isBeforeFirst()) {
                    while (rs.next()) {
                        String id = rs.getString("id");
                        String name = rs.getString("name");
                        String tData[] = {id, name};

                        tbModel.addRow(tData);
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "No Results");
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        if (allDepartment.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select Something to Delete First");
        } else if (dptName.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Department Name can't Be Empty");
        } else {
            int choice = JOptionPane.showConfirmDialog(null, "Are U sure To delete", "confirm", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                int selectedRow = allDepartment.getSelectedRow();
                DefaultTableModel tbModel = (DefaultTableModel) allDepartment.getModel();
                int thId = Integer.parseInt(tbModel.getValueAt(selectedRow, 0).toString());

                DepartmentDao dptDao = new DepartmentDao();
                Integer rowAffected = dptDao.delete(thId);

                if (rowAffected != null) {
                    JOptionPane.showMessageDialog(this, "Data Deleted");
                    dptName.setText("");
                    allDepartmentRetrived();
                } else {
                    JOptionPane.showMessageDialog(this, "Data not Deleted");
                }
            }

        }

    }//GEN-LAST:event_deleteBtnActionPerformed

    private void updateDptBtn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateDptBtn1ActionPerformed
        if (allDepartment.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(this, "Select Something to Update First");
        } else if (dptName.getText().trim().isEmpty() || dptName.getText().length() < 4) {
            JOptionPane.showMessageDialog(this, "Name not Valid \nMake sure not Empty \nMake sure not less that 4 char");
        } else {
            int selectedRow = allDepartment.getSelectedRow();
            DefaultTableModel tbModel = (DefaultTableModel) allDepartment.getModel();
            Department newDepartment = new Department();

            int thId = Integer.parseInt(tbModel.getValueAt(selectedRow, 0).toString());
            newDepartment.setId(thId);
            newDepartment.setName(dptName.getText());

            DepartmentDao dptDao = new DepartmentDao();
            Integer rowAffected = dptDao.update(newDepartment);

            if (rowAffected != null) {
                JOptionPane.showMessageDialog(this, "Data updated");
                dptName.setText("");
                allDepartmentRetrived();
            } else {
                JOptionPane.showMessageDialog(this, "Data not updated");
            }
        }
    }//GEN-LAST:event_updateDptBtn1ActionPerformed

    private void addDptBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addDptBtnActionPerformed
        if(allDepartment.getSelectedRow() != -1){
            JOptionPane.showMessageDialog(this, "Diselect a selected row To insert");
        }else if (dptName.getText().trim().isEmpty() || dptName.getText().length() < 4) {
            JOptionPane.showMessageDialog(this, "Name not Valid \nMake sure not Empty \nMake sure not less that 4 char");
        } else {
            Department newDepartment = new Department();
            newDepartment.setName(dptName.getText());

            DepartmentDao newDepartmentDao = new DepartmentDao();
            Integer rowsAffected = newDepartmentDao.register(newDepartment);

            if (rowsAffected != null) {
                JOptionPane.showMessageDialog(this, "Data Saved");
                dptName.setText("");
                allDepartmentRetrived();
            } else {
                JOptionPane.showMessageDialog(this, "Data not Saved");
            }
        }

    }//GEN-LAST:event_addDptBtnActionPerformed

